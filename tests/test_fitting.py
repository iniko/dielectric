def test_per_component_msp_consistent_with_chi2() -> None:
    # weighted fit → mean of (msp_real, msp_imag) ≈ χ² / N  (Σ pull² = χ², N = 2·n_freq)
    truth = MultiPoleRelaxation(5.0, ((52.0, 8e-12, 0.05),), sigma_dc=0.7)
    rng = np.random.default_rng(0)
    base = truth.epsilon(F)
    reps = [base + rng.normal(0, 0.05, F.size) + 1j * rng.normal(0, 0.05, F.size) for _ in range(8)]
    from dielectric.uncertainty.typea import combine_repeats

    ta = combine_repeats(tuple(Spectrum(F, r) for r in reps))
    res = fit_cole_cole_conductivity(ta.mean)
    assert np.isfinite(res.msp_real) and np.isfinite(res.msp_imag)
    assert (res.msp_real + res.msp_imag) / 2.0 == pytest.approx(res.chi2 / res.n_data, rel=1e-6)


def test_unweighted_r2_component_may_be_negative() -> None:
    # plain Debye on a conductive spectrum misfits ε'' badly → its component R² is the worse one
    s = _h02_like()
    with warnings.catch_warnings():
        warnings.simplefilter("ignore")
        res = fit_debye(s)
    assert np.isfinite(res.r_squared_imag)
    assert res.r_squared_imag < res.r_squared_real  # ε'' is the worse-fit component


def test_band_edge_pole_warning() -> None:
    # a pole forced far below the measured band should trip the band-edge disclosure
    s = _h02_like()  # band 0.2–20 GHz
    with warnings.catch_warnings():
        warnings.simplefilter("ignore")
        sel = select_model(s, force_model="Cole-Cole (2 poles) + DC σ")
    f_peaks = [
        1.0 / (2.0 * np.pi * v) for k, v in sel.chosen.result.params.items() if k.startswith("tau")
    ]
    out_of_band = any(fp < 0.2e9 or fp > 20e9 for fp in f_peaks)
    has_warning = any("outside the measured band" in w for w in sel.warnings)
    assert has_warning == out_of_band
