def test_compose_fitter_rejects_invalid_combinations() -> None:
    for family in ("Cole-Davidson", "Havriliak-Negami", "Jonscher"):
        with pytest.raises(ValueError, match=r"Debye|Cole-Cole"):
            compose_fitter(family, n_poles=2)
        with pytest.raises(ValueError, match=r"Debye|Cole-Cole"):
            compose_fitter(family, dc_sigma=True)
    with pytest.raises(ValueError, match="unknown model family"):
        compose_fitter("Nope")
    with pytest.raises(ValueError, match="n_poles must be"):
        compose_fitter("Debye", n_poles=0)


